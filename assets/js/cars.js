'use strict';

const buildCarImage = ({ make, modelFamily, year, angle = '23' }) => {
  const url = new URL('https://cdn.imagin.studio/getImage');

  url.searchParams.set('customer', 'img');
  url.searchParams.set('make', make);
  url.searchParams.set('modelFamily', modelFamily);
  url.searchParams.set('modelYear', String(year));
  url.searchParams.set('zoomType', 'fullscreen');
  url.searchParams.set('angle', angle);

  return url.toString();
};

